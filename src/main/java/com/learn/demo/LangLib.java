package com.learn.demo;

public class LangLib {

    public static void main(String[] args) {

        Integer number = 42;
        String text = "Hello, World!";
        Boolean isTrue = true;
        Double decimal = 3.14;
        Character letter = 'A';
        Float pi = 3.14f;

        System.out.println("Number: " + number.toString());
        System.out.println("Text: " + text.toString());
        System.out.println("Boolean: " + isTrue.toString());
        System.out.println("Decimal: " + decimal.toString());
        System.out.println("Character: " + letter.toString());
        System.out.println("Float: " + pi.toString());

    }
}
